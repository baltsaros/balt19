#include "fractol.h"

int	ft_mini_atoi(const char *str)
{
	int	n;

	n = 0;
	if ((str[0] < 49 || str[0] > 51) && str[1])
		return (-1);
	n = n * 10 + str[0] - '0';
	return (n);
}
